<h1>Welcome to <em>Seriously, that's not funny!</em></h1> 
<p>Listen to the first episode!</p>
<p><iframe src="https://w.soundcloud.com/player/?url=https%3A//api.soundcloud.com/users/126432593&amp;color=527f68&amp;auto_play=false&amp;hide_related=false&amp;show_comments=true&amp;show_user=true&amp;show_reposts=false"></iframe></p>

<p>This is a podcast devoted to the best way to make "dad jokes," <em>worth</em> listening to. Some have called it "<em>Car Talk</em> <small>(with Ian and Matt!)</small> for jokes". We do not discourage this interpretation. Both of us have spent many years (53 years combined!) working in theatre, film, stand-up, sketch comedy, burlesque, and other less funny endeavors. We have been writers, producers, directors, performers, schmucks carrying stuff, and numerous less illustrious roles (I'm not talking about you Art Department&mdash;<strong>you</strong> are the unsung gods of all creative work. Reach out to us and we will give you some time in the sun.) So let's just say, we've heard some jokes over the years.</p> 

<p>Jokes are ideas, and a forum, that help you stretch and test your relationship with reality. We hope to stretch it to breaking point. </p>

<p>If you have a joke or a piece of a joke, or a punch line that desperately needs a set up... please <a href="contact.php">let us know</a>. We will find it, or tell you why we couldn't, and then attempt to make it funny.</p>

<p>A good friend of ours told us "Why don't you call your podcast: 'Ian and Matt ruin jokes'?" We have yet to disagree with him because we are still laughing.</p>

<p id="small"><small><strong>DISCLAIMER:</strong> All humor is highly subjective, and dependent on mutiple nebulous factors that we try to take into account in our discussions. "I thinksk, therfore I ams" -Popeye: is the closest we have to a fact we can base our study on.</small>
